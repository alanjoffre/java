
public class OperadoresLogicosIfElse {

	public static void main(String[] args) {
		
		// > maior
		// < menor
		// >= maior ou igual
		// <= menor ou igual
		// == igual
		// != diferente
		
		if(1 != 2) {
			System.out.println("Verdadeiro.");
		} else {
			System.out.println("Falso.");
		}

	}

}
